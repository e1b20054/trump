package group9.trump.model;

public class Turn {
  int id;
  String name;
  int tehuda;
  int turn;

  public int getId() {
    return id;
  }
  public void setId(int id) {
    this.id = id;
  }
  public String getName() {
    return name;
  }
  public void setName(String name) {
    this.name = name;
  }
  public int getTehuda() {
    return tehuda;
  }
  public void setTehuda(int tehuda) {
    this.tehuda = tehuda;
  }
  public int getTurn() {
    return turn;
  }
  public void setTurn(int turn) {
    this.turn = turn;
  }



}
