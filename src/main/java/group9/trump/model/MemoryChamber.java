package group9.trump.model;

public class MemoryChamber {
  int id;
  String name;
  int roomNo;
  boolean oya;

  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public int getRoomNo() {
    return roomNo;
  }

  public void setRoomNo(int roomNo) {
    this.roomNo = roomNo;
  }

  public boolean getOya() {
    return oya;
  }

  public void setOya(boolean oya) {
    this.oya = oya;
  }

}
