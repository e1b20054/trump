package group9.trump.model;

public class Trump {
  int id;
  int number;
  String mark;

  public int getId() {
    return id;
  }
  public void setId(int id) {
    this.id = id;
  }
  public int getNumber() {
    return number;
  }
  public void setNumber(int num) {
    this.number = num;
  }
  public String getMark() {
    return mark;
  }
  public void setMark(String mark) {
    this.mark = mark;
  }

}
