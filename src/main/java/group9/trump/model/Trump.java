package group9.trump.model;

public class Trump {
  int id;
  int num;
  String mark;

  public int getId() {
    return id;
  }
  public void setId(int id) {
    this.id = id;
  }
  public int getNum() {
    return num;
  }
  public void setNum(int num) {
    this.num = num;
  }
  public String getMark() {
    return mark;
  }
  public void setMark(String mark) {
    this.mark = mark;
  }

}
